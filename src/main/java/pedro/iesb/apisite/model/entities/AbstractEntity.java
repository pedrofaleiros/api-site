package pedro.iesb.apisite.model.entities;

public abstract class AbstractEntity {

    protected String name;
    protected String id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
